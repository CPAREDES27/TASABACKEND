package com.incloud.hcp.jco.tripulantes.dto;

public class CertificadoDto {

    private String CDCER;
    private String NRCER;
    private String FECCF;
    private String DSCER;

    public String getDSCER() {
        return DSCER;
    }

    public void setDSCER(String DSCER) {
        this.DSCER = DSCER;
    }

    public String getCDCER() {
        return CDCER;
    }

    public void setCDCER(String CDCER) {
        this.CDCER = CDCER;
    }

    public String getNRCER() {
        return NRCER;
    }

    public void setNRCER(String NRCER) {
        this.NRCER = NRCER;
    }

    public String getFECCF() {
        return FECCF;
    }

    public void setFECCF(String FECCF) {
        this.FECCF = FECCF;
    }
}
