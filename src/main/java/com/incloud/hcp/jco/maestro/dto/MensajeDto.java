package com.incloud.hcp.jco.maestro.dto;

public class MensajeDto {
    private String MANDT;
    private String CMIN;
    private String CDMIN;
    private String DSMIN;

    public String getMANDT() {
        return MANDT;
    }

    public void setMANDT(String MANDT) {
        this.MANDT = MANDT;
    }

    public String getCMIN() {
        return CMIN;
    }

    public void setCMIN(String CMIN) {
        this.CMIN = CMIN;
    }

    public String getCDMIN() {
        return CDMIN;
    }

    public void setCDMIN(String CDMIN) {
        this.CDMIN = CDMIN;
    }

    public String getDSMIN() {
        return DSMIN;
    }

    public void setDSMIN(String DSMIN) {
        this.DSMIN = DSMIN;
    }
}
