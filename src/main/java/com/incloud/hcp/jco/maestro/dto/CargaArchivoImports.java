package com.incloud.hcp.jco.maestro.dto;

public class CargaArchivoImports {

    private String p_user;
    private String p_code;
    private String p_change;
    private String p_valida;

    public String getP_user() {
        return p_user;
    }

    public void setP_user(String p_user) {
        this.p_user = p_user;
    }

    public String getP_code() {
        return p_code;
    }

    public void setP_code(String p_code) {
        this.p_code = p_code;
    }

    public String getP_change() {
        return p_change;
    }

    public void setP_change(String p_change) {
        this.p_change = p_change;
    }

    public String getP_valida() {
        return p_valida;
    }

    public void setP_valida(String p_valida) {
        this.p_valida = p_valida;
    }

}
